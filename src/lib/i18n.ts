const RTL_LOCALES = new Set(['ar']);

export function getLocale(): string {
  const lang = (navigator.language || 'en').toLowerCase();
  if (lang in translations) return lang;
  const base = lang.split('-')[0];
  return base;
}

/**
 * Return the full BCP 47 tag for the HTML lang attribute.
 */
export function getHtmlLang(): string {
  return navigator.language || 'en';
}

/**
 * Only apply RTL if we actually have translations for the locale.
 * Otherwise the user gets English text in a right-to-left layout.
 */
export function isRTL(locale: string): boolean {
  return RTL_LOCALES.has(locale) && locale in translations;
}

export function getTranslation(locale: string): Translation {
  return translations[locale] ?? translations['en'];
}
